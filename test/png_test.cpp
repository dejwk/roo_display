std::string RunfilesRoot() {
  const char* test_srcdir = std::getenv("TEST_SRCDIR");
  const char* test_workspace = std::getenv("TEST_WORKSPACE");
  if (test_srcdir == nullptr || test_workspace == nullptr) {
    return {};
  }
  return std::string(test_srcdir) + "/" + test_workspace;
}

class PosixTestFilesystem : public roo_io::Filesystem {
 public:
  explicit PosixTestFilesystem(std::string mount_point)
      : mount_point_(std::move(mount_point)) {}

  MediaPresence checkMediaPresence() override { return kMediaPresent; }

 protected:
  roo_io::MountImpl::MountResult mountImpl(
      std::function<void()> unmount_fn) override {
    return roo_io::MountImpl::Mounted(
        std::unique_ptr<roo_io::MountImpl>(new roo_io::PosixMountImpl(
            mount_point_.c_str(), true, std::move(unmount_fn))));
  }

  void unmountImpl() override {}

 private:
  std::string mount_point_;
};

void Draw(DisplayDevice& output, const Drawable& object,
          FillMode fill_mode = FillMode::kVisible,
          BlendingMode blending_mode = BlendingMode::kSourceOver,
          Color bgcolor = color::Transparent) {
  output.begin();
  Box clip_box(0, 0, output.effective_width() - 1,
               output.effective_height() - 1);
  Surface s(output, 0, 0, clip_box, false, bgcolor, fill_mode, blending_mode);
  s.drawObject(object);
  output.end();
}

