TEST(Png, PaletteOpaqueFile) {
  std::string runfiles_root = RunfilesRoot();
  ASSERT_FALSE(runfiles_root.empty());

  PosixTestFilesystem fs(runfiles_root);
  PngDecoder decoder;
  PngFile image(decoder, fs, kPaletteImagePath);

  FakeOffscreen<Argb8888> test_screen(8, 4, color::Transparent);
  Draw(test_screen, image, FillMode::kVisible, BlendingMode::kSource);

  EXPECT_THAT(test_screen,
              MatchesContent(Argb8888(), 8, 4,
                             "FFFF0000 FF00FF00 FF0000FF FFFFFF00 "
                             "FF00FFFF FFFF00FF FFFFFFFF FF000000"
                             "FF804000 FFFF8000 FF80FF00 FF008080 "
                             "FF8000FF FFFF80C0 FF808080 FF004080"
                             "FF000000 FFFFFFFF FFFF00FF FF00FFFF "
                             "FFFFFF00 FF0000FF FF00FF00 FFFF0000"
                             "FF004080 FF808080 FFFF80C0 FF8000FF "
                             "FF008080 FF80FF00 FFFF8000 FF804000"));
}

